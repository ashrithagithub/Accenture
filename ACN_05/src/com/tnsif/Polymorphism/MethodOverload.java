package com.tnsif.Polymorphism;

public class MethodOverload {
	public void show()
	{
		System.out.println("This is show method with 0 parameters");
	}
	public void show(int a)
	{
		System.out.println("This is show method with 0 parameters "+a);
	}
	public void show(int a, double b)
	{
		System.out.println("This is show method with 0 parameters "+a +" and " +b);
	}
	public void show(double a, int b)
	{
		System.out.println("This is show method with 0 parameters "+a +" and " +b);
	}
	public void show(int a, int b, int c)
	{
		System.out.println("This is show method with 0 parameters"+a +" and " +b +" and "+ c);
	}




}
