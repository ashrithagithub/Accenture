package com.tnsif.day2;

public class Loops {

	public static void main(String[] args) {
//for loop
		for(int i=1; i<=10; i++) {
			System.out.print(i);
		}
		
System.out.println(" ");

//while loop
		int num = 10;
		int n = 1;
		while(n<=num) {
			System.out.print(n);
			n++;
		}

System.out.println(" ");

//do while loop Ex-1
		int j=1;
		do {
			System.out.print(j);
			j++;
		}
		while(j<=10);
		

System.out.println(" ");

//do while loop EX-2
		int k=1;
		do {
			System.out.print(k);
			k++;
		}
		while(k>=10);
			

	}

}
