package com.tnsif.day1;

public class datatypes {

	public static void main(String[] args) {
		byte b = 0123;
		short s = 3284;
		int i  = 376732;
		long l = 318643567;
		float f = 3.458f;
		double d = 463.9876543123456;
		
		char ch= 'a';
		boolean boo = false;
		
		System.out.println("the byte vale is: " +b);
		System.out.println("the short vale is: " +s);
		System.out.println("the int vale is: " +i);
		System.out.println("the long vale is: " +l);
		System.out.println("the float vale is: " +f);
		System.out.println("the double vale is: " +d);
		System.out.println("the char vale is: " +ch);
		System.out.println("the boolean vale is: " +boo);
		

	}

}
